'use client';
import { useEffect, useState, Suspense } from 'react';
import { useRouter, useSearchParams } from 'next/navigation';
import { loadUserData, markIssueFixed, getBadgeInfo, gradeColor, addScanResult } from '@/lib/shield-storage';
import { ScanResult } from '@/lib/shield-types';
import ShieldAppLayout from '@/app/shield/components/ShieldAppLayout';
import ShieldSecurityScore from '@/app/shield/components/ShieldSecurityScore';
import ShieldVulnerabilityCard from '@/app/shield/components/ShieldVulnerabilityCard';
import {
  Shield, CheckCircle, AlertTriangle, Download,
  Share2, ArrowLeft, Trophy, Loader2
} from 'lucide-react';

const GRADE_MESSAGES: Record<string, string> = {
  A: 'Outstanding security posture. Your application is well-protected.',
  B: 'Good security with minor improvements possible.',
  C: 'Several issues need attention. Fix Critical items first.',
  D: 'Multiple vulnerabilities found. Fix Critical and High issues immediately.',
  F: 'Serious security risks detected. This application needs immediate attention.',
};

const GRADE_COLORS: Record<string, string> = {
  A: '#22c55e', B: '#34d399', C: '#f59e0b', D: '#f97316', F: '#ef4444',
};

function ResultsContent() {
  const router = useRouter();
  const params = useSearchParams();
  const scanId = params.get('id');

  const [result, setResult] = useState<ScanResult | null>(null);
  const [mode, setMode] = useState<'beginner' | 'advanced'>('beginner');
  const [shareMsg, setShareMsg] = useState('');

  useEffect(() => {
    const userData = loadUserData();
    if (!userData) { router.push('/shield/login'); return; }
    setMode(userData.mode);
    if (scanId) {
      const scan = userData.scanHistory.find(s => s.id === scanId);
      if (scan) setResult(scan);
    } else {
      const pending = sessionStorage.getItem('lc3shield_pending_result');
      if (pending) {
        const parsed: ScanResult = JSON.parse(pending);
        addScanResult(parsed);
        sessionStorage.removeItem('lc3shield_pending_result');
        setResult(parsed);
      }
    }
  }, [router, scanId]);

  const handleFixed = (vulnId: string) => {
    if (!result) return;
    markIssueFixed(result.id, vulnId);
    const userData = loadUserData();
    const updated = userData?.scanHistory.find(s => s.id === result.id);
    if (updated) setResult({ ...updated });
  };

  const handleShare = () => {
    navigator.clipboard.writeText(window.location.href).then(() => {
      setShareMsg('Copied!');
      setTimeout(() => setShareMsg(''), 2000);
    });
  };

  const handleDownload = () => {
    if (!result) return;
    const vulnRows = result.vulnerabilities.map((v, i) =>
      `<tr style="border-bottom:1px solid #eee">
        <td style="padding:8px">${i + 1}</td>
        <td style="padding:8px;font-weight:700;color:${v.severity === 'Critical' ? '#dc2626' : v.severity === 'High' ? '#ea580c' : v.severity === 'Medium' ? '#ca8a04' : '#2563eb'}">${v.severity}</td>
        <td style="padding:8px">${v.title}</td>
        <td style="padding:8px;font-size:12px">${v.fix}</td>
      </tr>`).join('');
    const html = `<!DOCTYPE html><html><head><title>LC3 Shield Report — ${result.target}</title>
    <style>body{font-family:sans-serif;max-width:900px;margin:40px auto;color:#111;line-height:1.6}
    h1{color:#1d4ed8}table{width:100%;border-collapse:collapse;margin-top:12px}
    th{background:#f1f5f9;padding:10px;text-align:left;font-weight:600}</style></head><body>
    <h1>🛡️ LC3 Shield Security Report</h1>
    <p><strong>Target:</strong> ${result.target}</p>
    <p><strong>Scan Date:</strong> ${new Date(result.timestamp).toLocaleString()}</p>
    <p><strong>Score:</strong> ${result.score}/100 &nbsp;|&nbsp; <strong>Grade:</strong> ${result.grade}</p>
    <p><strong>Summary:</strong> ${result.summary}</p>
    <h2>Vulnerabilities (${result.vulnerabilities.length})</h2>
    <table><thead><tr><th>#</th><th>Severity</th><th>Issue</th><th>Recommended Fix</th></tr></thead>
    <tbody>${vulnRows || '<tr><td colspan="4" style="padding:16px;text-align:center">✅ No vulnerabilities found!</td></tr>'}</tbody></table>
    <p style="margin-top:40px;color:#888;font-size:12px">Generated by LC3 Shield · Powered by Claude AI</p>
    </body></html>`;
    const blob = new Blob([html], { type: 'text/html' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `lc3-shield-report-${Date.now()}.html`;
    a.click();
    URL.revokeObjectURL(url);
  };

  if (!result) return (
    <ShieldAppLayout>
      <div className="flex items-center justify-center" style={{ height: '80vh' }}>
        <Loader2 className="w-8 h-8 text-blue-400 animate-spin" />
      </div>
    </ShieldAppLayout>
  );

  const gradeHexColor = GRADE_COLORS[result.grade] || GRADE_COLORS['F'];
  const gradeMessage = GRADE_MESSAGES[result.grade] || GRADE_MESSAGES['F'];
  const criticalCount = result.vulnerabilities.filter(v => v.severity === 'Critical').length;
  const highCount = result.vulnerabilities.filter(v => v.severity === 'High').length;
  const mediumCount = result.vulnerabilities.filter(v => v.severity === 'Medium').length;
  const lowCount = result.vulnerabilities.filter(v => v.severity === 'Low').length;
  const fixedVulns = result.vulnerabilities.filter(v => v.isFixed).length;

  const severityBreakdown = [
    { label: 'Critical', count: criticalCount, color: '#ef4444', bg: 'rgba(239,68,68,0.12)', border: 'rgba(239,68,68,0.3)' },
    { label: 'High', count: highCount, color: '#f97316', bg: 'rgba(249,115,22,0.12)', border: 'rgba(249,115,22,0.3)' },
    { label: 'Medium', count: mediumCount, color: '#f59e0b', bg: 'rgba(245,158,11,0.12)', border: 'rgba(245,158,11,0.3)' },
    { label: 'Low', count: lowCount, color: '#60a5fa', bg: 'rgba(59,130,246,0.12)', border: 'rgba(59,130,246,0.3)' },
  ].filter(s => s.count > 0);

  return (
    <ShieldAppLayout>
      <div className="px-4 sm:px-6 py-6 sm:py-8 max-w-4xl mx-auto">

        {/* Back */}
        <button
          onClick={() => router.back()}
          className="flex items-center gap-2 mb-6 transition-colors"
          style={{ fontSize: '14px', color: '#94a3b8' }}
          onMouseEnter={(e) => (e.currentTarget.style.color = '#e2e8f0')}
          onMouseLeave={(e) => (e.currentTarget.style.color = '#94a3b8')}
        >
          <ArrowLeft className="w-4 h-4" /> Back
        </button>

        {/* Score card */}
        <div
          className="rounded-2xl overflow-hidden mb-6"
          style={{ background: '#161b27', border: '1px solid rgba(255,255,255,0.09)' }}
        >
          <div className="h-1" style={{ background: `linear-gradient(90deg, ${gradeHexColor}, transparent)` }} />

          <div className="p-7">
            <div className="flex flex-col md:flex-row items-start md:items-center gap-7">
              <div className="shrink-0">
                <ShieldSecurityScore score={result.score} grade={result.grade} showBar />
              </div>

              <div className="flex-1 min-w-0">
                <div style={{ fontSize: '11px', fontWeight: 700, color: '#64748b', textTransform: 'uppercase', letterSpacing: '0.08em', marginBottom: '4px' }}>
                  Scan Target
                </div>
                <div className="font-semibold text-white mb-3 truncate" style={{ fontSize: '16px' }}>
                  {result.target}
                </div>

                <p style={{ fontSize: '13px', color: gradeHexColor, marginBottom: '14px' }}>{gradeMessage}</p>

                {/* Severity breakdown */}
                <div className="flex flex-wrap gap-2 mb-5">
                  {severityBreakdown.length === 0 ? (
                    <span
                      className="flex items-center gap-1.5 px-3 py-1 rounded-full text-[13px] font-semibold"
                      style={{ background: 'rgba(34,197,94,0.12)', color: '#4ade80', border: '1px solid rgba(34,197,94,0.3)' }}
                    >
                      <CheckCircle className="w-3.5 h-3.5" /> No issues found
                    </span>
                  ) : (
                    severityBreakdown.map(({ label, count, color, bg, border }) => (
                      <span
                        key={label}
                        className="px-3 py-1 rounded-full text-[12px] font-semibold"
                        style={{ background: bg, color, border: `1px solid ${border}` }}
                      >
                        {count} {label}
                      </span>
                    ))
                  )}
                  {fixedVulns > 0 && (
                    <span
                      className="flex items-center gap-1.5 px-3 py-1 rounded-full text-[12px] font-semibold"
                      style={{ background: 'rgba(34,197,94,0.12)', color: '#4ade80', border: '1px solid rgba(34,197,94,0.3)' }}
                    >
                      <CheckCircle className="w-3.5 h-3.5" /> {fixedVulns} Fixed
                    </span>
                  )}
                </div>

                {/* Actions */}
                <div className="flex gap-2 flex-wrap">
                  <button
                    onClick={handleDownload}
                    className="flex items-center gap-2 px-4 py-2 rounded-lg text-[13px] font-medium transition-all"
                    style={{ background: 'rgba(59,130,246,0.12)', color: '#93c5fd', border: '1px solid rgba(59,130,246,0.25)' }}
                    onMouseEnter={(e) => (e.currentTarget.style.background = 'rgba(59,130,246,0.2)')}
                    onMouseLeave={(e) => (e.currentTarget.style.background = 'rgba(59,130,246,0.12)')}
                  >
                    <Download className="w-4 h-4" /> Download Report
                  </button>
                  <button
                    onClick={handleShare}
                    className="flex items-center gap-2 px-4 py-2 rounded-lg text-[13px] font-medium transition-all"
                    style={{ background: 'rgba(255,255,255,0.06)', color: '#94a3b8', border: '1px solid rgba(255,255,255,0.1)' }}
                    onMouseEnter={(e) => (e.currentTarget.style.color = '#e2e8f0')}
                    onMouseLeave={(e) => (e.currentTarget.style.color = '#94a3b8')}
                  >
                    <Share2 className="w-4 h-4" /> {shareMsg || 'Share'}
                  </button>
                </div>
              </div>
            </div>
          </div>
        </div>

        {/* Badges earned */}
        {result.badgesEarned.length > 0 && (
          <div
            className="rounded-2xl p-5 mb-6"
            style={{ background: 'rgba(245,158,11,0.07)', border: '1px solid rgba(245,158,11,0.25)' }}
          >
            <div className="flex items-center gap-2 mb-3">
              <Trophy className="w-4 h-4 text-amber-400" />
              <span style={{ fontSize: '13px', fontWeight: 700, color: '#fbbf24' }}>Badges Earned!</span>
            </div>
            <div className="flex flex-wrap gap-3">
              {result.badgesEarned.map(id => {
                const badge = getBadgeInfo(id);
                return (
                  <div
                    key={id}
                    className="flex items-center gap-2.5 px-3.5 py-2.5 rounded-xl"
                    style={{ background: 'rgba(245,158,11,0.1)', border: '1px solid rgba(245,158,11,0.25)' }}
                  >
                    <span className="text-xl">{badge.icon}</span>
                    <div>
                      <div style={{ fontSize: '13px', fontWeight: 700, color: '#fbbf24' }}>{badge.name}</div>
                      <div style={{ fontSize: '11px', color: '#a16207' }}>{badge.description}</div>
                    </div>
                  </div>
                );
              })}
            </div>
          </div>
        )}

        {/* Summary */}
        <div
          className="rounded-2xl p-5 mb-6"
          style={{ background: '#161b27', border: '1px solid rgba(255,255,255,0.09)' }}
        >
          <div className="flex items-center gap-2 mb-2">
            <Shield className="w-4 h-4 text-blue-400" />
            <span style={{ fontSize: '13px', fontWeight: 700, color: '#93c5fd' }}>Summary</span>
          </div>
          <p style={{ fontSize: '14px', color: '#cbd5e1', lineHeight: '1.7' }}>{result.summary}</p>
        </div>

        {/* Vulnerabilities */}
        {result.vulnerabilities.length > 0 ? (
          <div>
            <div className="flex items-center gap-2.5 mb-4">
              <AlertTriangle className="w-4 h-4 text-red-400" />
              <h2 className="text-white font-bold" style={{ fontSize: '17px', letterSpacing: '-0.02em' }}>
                Issues Found
              </h2>
              <span
                className="px-2 py-0.5 rounded-full text-[12px] font-bold"
                style={{ background: 'rgba(239,68,68,0.12)', color: '#fca5a5', border: '1px solid rgba(239,68,68,0.25)' }}
              >
                {result.vulnerabilities.length}
              </span>
              <span style={{ fontSize: '13px', color: '#64748b' }}>· Fix Critical first</span>
            </div>

            <div className="space-y-3">
              {(['Critical', 'High', 'Medium', 'Low', 'Info'] as const).flatMap(sev =>
                result.vulnerabilities
                  .filter(v => v.severity === sev)
                  .map(vuln => (
                    <ShieldVulnerabilityCard
                      key={vuln.id}
                      vuln={vuln}
                      mode={mode}
                      scanId={result.id}
                      onFixed={handleFixed}
                    />
                  ))
              )}
            </div>
          </div>
        ) : (
          <div
            className="text-center py-20 rounded-2xl"
            style={{ background: 'rgba(34,197,94,0.05)', border: '1px solid rgba(34,197,94,0.2)' }}
          >
            <CheckCircle className="w-14 h-14 text-emerald-400 mx-auto mb-4" />
            <h2 className="text-xl font-bold text-white mb-2">No Issues Found!</h2>
            <p style={{ fontSize: '14px', color: '#94a3b8' }}>
              {mode === 'beginner'
                ? 'Your security looks great! Keep scanning regularly to catch issues early.'
                : 'No vulnerabilities detected. Continue monitoring regularly.'}
            </p>
          </div>
        )}
      </div>
    </ShieldAppLayout>
  );
}

export default function ResultsPage() {
  return (
    <Suspense fallback={
      <ShieldAppLayout>
        <div className="flex items-center justify-center" style={{ height: '80vh' }}>
          <Loader2 className="w-8 h-8 text-blue-400 animate-spin" />
        </div>
      </ShieldAppLayout>
    }>
      <ResultsContent />
    </Suspense>
  );
}
